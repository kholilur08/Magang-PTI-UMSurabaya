                                <h2 class="title-default-left-bold-lowhight"><a href="#">Universitas Muhammadiyah Surabaya Terima Mahasiswa Pakai Nilai UTBK</a></h2>
                                <ul class="title-bar-high news-comments">
                                    <li><a href="#"><i class="fa fa-user" aria-hidden="true"></i><span>By</span> Admin</a></li>
                                    <li><a href="#"><i class="fa fa-tags" aria-hidden="true"></i>Admin</a></li>
                                    <li><a href="#"><i class="fa fa-comments-o" aria-hidden="true"></i></a></li>
                                </ul>
                                <p>Pengumuman jalur SBMPTN 2021 telah resmi dikeluarkan oleh LTMPT pada Senin (14/6/2021) lalu. Bagi yang tak lolos, Universitas Muhammadiyah Surabaya (UMSurabaya) memberikan kesempatan untuk menggunakan skor/nilai Ujian Tulis Berbasis Komputer (UTBK) sebagai syarat masuk perguruan tinggi tersebut. 



 
</p>
                                <p>
Ma’ruf Sya’ban, wakil rektor bidang kemahasiswaan, inovasi dan SDM menjelaskan bahwa kebijakan ujian masuk menggunakan nilai UTBK sudah berjalan sejak dua tahun lalu. Hal tersebut dilakukan sebagai upaya memudahkan para pendaftar, dan terbukti efektif membantu proses pendaftaran.
</p>
<p>

“Tes UTBK sudah pasti kredibel untuk mengukur kemampuan awal calon mahasiswa. Proses pendaftaran semua prodi di UMSurabaya bisa menggunakan skor tes tersebut. Tentunya disesuaikan dengan pembagian kelompok ujian (saintek, soshum dan campuran).” Ujar Ma’ruf dalam keterangan tertulis, Rabu (16/6/2021). 
 </p>
 <p>

Radius Setiyawan, Kepala Lembaga Informasi dan Penerimaan Mahasiswa Baru (LIPMB) menjelaskan secara detail ketentuan jalur tersebut. Teknis alur pendaftarannya tidak jauh berbeda dengan jalur regular. Yang membedakan adalah pendaftar jalur ini dibebaskan dari seleksi Computer Based Test (CBT) apabila skor UTBK-nya sesuai dengan standart yang telah ditentukan. 


 </p>

“Pendaftar dapat menggunakan skor UTBK dua tahun terakhir (2020 dan 2021). Setelah melakukan pendaftaran online, menyerahkan sertifikat skor UTBK dan dinyatakan lulus, pendaftar bisa langsung melakukan daftar ulang. Khusus pendaftar di Fakultas Kedokteran (FK) dan Fakultas Ilmu Kesehatan (FIK) akan diumumkan secara bersama pada tanggal 12 Juli 2021, dan yang dinyatakan lulus harus melalui tahapan ujian selanjutnya, yakni : uji kesehatan, psikologi dan wawancara” jelas Radius. 



</p>
                                <p>
Salman Al-Farisi, selaku sekretaris Lazizmu UMSurabaya menyampaikan bahwa ratusan paket sehat untuk Isoman terdiri dari 2 paket. Paket A terdiri dari Susu, Vitamin, Beras, Roti, Masker, Madu. Paket B khusus bagi yang sudah berkeluarga dan punya bayi yang terdiri dari Popok Bayi, Biskuit Bayi, Vitamin, Roti dan Masker.

 

"Setiap hari kami siapkan 50-100 paket gratis untuk keluarga yang isolasi" ujar Salman.

 

Salman menambahkan bahwa proses antar ke masing-masing rumah akan melibatkan ojek online. Adapun ketentuan proses antar tetap harus berpedoman pada protokol kesehatan yang ketat.

 

Warga  Surabaya yang menbutuhkan paket tersebut bisa menghubungi saudara Salman : 089654453687 dan

Rahman : 081216556554.</p>
