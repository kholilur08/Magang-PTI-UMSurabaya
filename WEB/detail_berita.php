                                <p>Melonjaknya kembali kasus COVID-19 mengakibatkan daya tampung Rumah Sakit Rujukan dan Darurat COVID mengalami over capacity. Sebagian Rumah Sakit harus menolak merawat pasien COVID-19, dan terpaksa harus menjalani Isolasi Mandiri (ISOMAN) di Rumah.

 
</p>
                                <p>
Menjalani ISOMAN di rumah akan terasa sangat sulit bagi mereka yang masih kesulitan dalam memenuhi kebutuhan sehari-hari mereka. Apalagi pasien Covid-19 dianjurkan untuk makan secara teratur, harus mengkonsumsi vitamin dan buah-buahan. Berdasarkan kondisi tersebut Universitas Muhammadiyah Surabaya (UM Surabaya) melalui Lazizmu UM Surabaya menyediakan paket sehat untuk pasien isolasi mandiri di Rumah.

 

Mundakir, Wakil Rektor UMSurabaya bidang Kerjasama, Digitalisasi dan AIK menyampaikan bahwa saat ini kita menghadapi krisis akibat lonjatan Covid-19. Banyak warga yang terpaksa harus menjalani Isolasi Mandiri di Rumah.

 

"UMSurabaya harus peduli kepada warga yang terinfeksi Covid-19, dan menjalani ISOMAN di rumah. Banyak dari mereka yang masih kesusahan sehingga perlu diberikan bantuan", ujar Mundakir dalam keterangan tertulis pada Kamis, (8/7/2021).

 

Dia juga mengungkapkan bahwa pembagian paket sehat untuk pasien Isoman ini adalah sumbangsih kampus di tengah situasi krisis saat ini, dan merupakan bentuk nyata dari spirit Al-Maun yaitu peduli kepada mereka yang lemah dan memerlukan bantuan.

</p>
                                <p>
Salman Al-Farisi, selaku sekretaris Lazizmu UMSurabaya menyampaikan bahwa ratusan paket sehat untuk Isoman terdiri dari 2 paket. Paket A terdiri dari Susu, Vitamin, Beras, Roti, Masker, Madu. Paket B khusus bagi yang sudah berkeluarga dan punya bayi yang terdiri dari Popok Bayi, Biskuit Bayi, Vitamin, Roti dan Masker.

 

"Setiap hari kami siapkan 50-100 paket gratis untuk keluarga yang isolasi" ujar Salman.

 

Salman menambahkan bahwa proses antar ke masing-masing rumah akan melibatkan ojek online. Adapun ketentuan proses antar tetap harus berpedoman pada protokol kesehatan yang ketat.

 

Warga  Surabaya yang menbutuhkan paket tersebut bisa menghubungi saudara Salman : 089654453687 dan

Rahman : 081216556554.</p>
